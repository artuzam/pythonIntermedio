def suma(a, b):
    """
    Realiza la suma de dos números enteros.

    Parámetros:
        a (int): El primer número entero.
        b (int): El segundo número entero.

    Retorna:
        int: La suma de los dos números.
    """
    return a + b


def resta(a, b):
    """
    Realiza la resta de dos números enteros.

    Parámetros:
        a (int): El número del que se va a restar.
        b (int): El número que se va a restar.

    Retorna:
        int: El resultado de la resta.
    """
    return a - b
